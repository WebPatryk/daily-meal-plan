/**
 * Meal Icon Helper
 * Provides utilities for working with meal icons
 */

export type MealIconName = 
  | "breakfast"    // śniadanie - jajka, płatki, tosty
  | "salad"        // sałatki, warzywa
  | "meat"         // mięso, kurczak
  | "fish"         // ryby, owoce morza
  | "pasta"        // makarony, dania z makaronem
  | "soup"         // zupy
  | "dessert"      // desery, słodycze
  | "fruit"        // owoce, smoothie
  | "vegetarian"   // dania wegańskie/wegetariańskie
  | "snack";       // przekąski

/**
 * Checks if an image_path string contains an icon reference
 */
export function isIconPath(imagePath: string | null | undefined): boolean {
  return !!imagePath && imagePath.startsWith("icon:");
}

/**
 * Extracts icon name from an icon path (format: "icon:category")
 */
export function extractIconName(imagePath: string | null | undefined): MealIconName | null {
  if (!isIconPath(imagePath)) return null;
  const iconName = imagePath!.replace("icon:", "");
  return iconName as MealIconName;
}

